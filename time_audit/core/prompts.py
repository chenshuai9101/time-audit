"""
时间审计 v2 — Prompt 模板（点 / 线 / 面）

每个模板：
  - SYSTEM: 角色与硬约束
  - USER:   待分析素材占位 {sessions} + 上下文 hint {context}
  - SCHEMA: 期望的 JSON 输出结构（同时塞进 prompt，作为 few-shot 约束）

设计原则：
  1. 输出严格 JSON（用 Ollama format=json）
  2. 字段名用英文，值用中文（避免本地小模型 JSON key 乱码）
  3. 每条结论必须带 evidence（引用 session id），便于核实与去幻觉
  4. confidence 三档：high/med/low
"""

# ─────────────────────────────────────────────────────────────────────────────
# 点：单次低效动作
# ─────────────────────────────────────────────────────────────────────────────
POINT_SYSTEM = """你是用户行为分析师，专门从屏幕录制日志中识别"单点低效动作"。

【单点动作】定义：用户在很短时间内（通常单个会话内）反复执行的、可被一段脚本/snippet替代的小操作。
典型例子：复制粘贴同一段固定文本、反复切换同两个App查值、重复点击同一菜单路径。

【硬要求】
- 必须基于真实证据，不要编造没出现过的操作
- 每条结论必须给出至少一个 session id 作为 evidence
- 如果数据里没有明显的单点低效，宁可返回空数组也不要硬凑
- 输出严格 JSON，不要任何前后缀文字"""

POINT_USER = """以下是用户最近的工作会话压缩日志（每段是一个会话）：

{context}

──── 会话日志开始 ────
{sessions}
──── 会话日志结束 ────

请找出最多 5 个值得做成 snippet/快捷键 skill 的单点低效动作，按以下 JSON schema 输出：

{{
  "points": [
    {{
      "title": "简短标题（中文，10字内）",
      "description": "这个动作是什么、用户怎么做的（中文，30字内）",
      "evidence_sessions": ["S001", "S005"],
      "frequency_hint": "出现次数描述，如 每天3次 / 每周5次",
      "skill_suggestion": "建议如何做成 skill（中文，30字内）",
      "confidence": "high|med|low"
    }}
  ]
}}"""


# ─────────────────────────────────────────────────────────────────────────────
# 线：跨 app 的固定流程
# ─────────────────────────────────────────────────────────────────────────────
LINE_SYSTEM = """你是流程挖掘分析师，从屏幕录制日志中识别"跨App固定工作流程"。

【线】定义：用户定期重复的、跨多个App、有明确开始与结束的多步流程。
典型例子：每周五"从CRM拉数据→Excel整理→邮件发送"；每天上午"打开HIS→查患者→Word填病历→保存"。

【硬要求】
- 必须看到流程在不同会话/不同天有重复才算"线"，单次操作不算
- 每条流程要给出可执行的步骤序列（用户视角，不是技术实现）
- 估算时间节省时基于会话实际耗时，不要凭空生成数字
- 输出严格 JSON，不要任何前后缀文字"""

LINE_USER = """以下是用户最近的工作会话压缩日志：

{context}

──── 会话日志开始 ────
{sessions}
──── 会话日志结束 ────

请找出最多 5 条值得 skill 化的跨App固定流程，按以下 JSON schema 输出：

{{
  "lines": [
    {{
      "workflow_name": "流程名称（中文，15字内）",
      "trigger": "触发条件描述，如 每天09:30 / 收到周报通知后",
      "apps_involved": ["App1", "App2"],
      "steps": [
        "步骤1：用户视角描述",
        "步骤2：..."
      ],
      "evidence_sessions": ["S001", "S010", "S020"],
      "occurrence_count": 数字（在数据中观察到的重复次数）,
      "avg_duration_min": 数字（单次平均耗时分钟）,
      "estimated_weekly_savings_min": 数字（自动化后每周省多少分钟，保守估计）,
      "skill_suggestion": "建议 skill 形态（中文，40字内）",
      "automation_difficulty": "low|med|high",
      "confidence": "high|med|low"
    }}
  ]
}}"""


# ─────────────────────────────────────────────────────────────────────────────
# 面：职业 / 角色级工作模式
# ─────────────────────────────────────────────────────────────────────────────
SURFACE_SYSTEM = """你是工作方式洞察分析师，从屏幕录制日志中识别"角色级工作模式"。

【面】定义：超越单点和单流程的、关于用户整体工作方式的判断。
典型例子：
- "用户像是医生：核心时间在病历填写与患者查询，门诊流程化但缺少结构化录入工具"
- "用户像是产品经理：60%时间在跨工具同步状态而非创造，应当引入异步看板而非更多自动化"
- "用户工作高度模板化但模板分散在Word/Excel/Notion三处，应当统一入口"

【硬要求】
- 不是 skill 建议，而是工作方式的高层洞察
- 每条洞察要既包含"现象"也包含"建议（可能是 skill，也可能是工作流程改造）"
- 一定要诚实，如果数据看不出明显的角色画像就少给或不给
- 输出严格 JSON，不要任何前后缀文字"""

SURFACE_USER = """以下是用户最近的工作会话压缩日志：

{context}

──── 会话日志开始 ────
{sessions}
──── 会话日志结束 ────

请给出最多 3 条角色级 / 工作方式级的洞察，按以下 JSON schema 输出：

{{
  "surfaces": [
    {{
      "insight_title": "洞察标题（中文，15字内）",
      "observation": "你看到的现象（中文，60字内）",
      "evidence_sessions": ["S001", "S015"],
      "implication": "这意味着什么（中文，60字内）",
      "recommendation": "建议怎么改（中文，60字内，可以是skill建议也可以是工作流程改造建议）",
      "confidence": "high|med|low"
    }}
  ]
}}"""


def render(template_user: str, sessions_text: str, context_hint: str) -> str:
    """填充 user prompt 占位符"""
    return template_user.format(sessions=sessions_text, context=context_hint)
